// -- Convenience helpers -------------------------------------------------

/**
 * Get the full class map for a color.
 * Returns the gray fallback if the color isn't mapped.
 */
export function getColor(color) {
  return variants[color] || variants.gray;
}

/**
 * Common composite patterns used across components.
 * Returns an object with pre-composed class strings.
 */
export function getColorComposite(color) {
  const c = getColor(color);
  return {
    // Icon container: soft background + colored icon
    iconContainer: `${c.bg50} flex items-center justify-center`,
    iconContainerLg: `${c.bg100} flex items-center justify-center`,
    icon: c.text600,

    // Badge: soft bg + dark text
    badge: `${c.bg50} ${c.text700}`,
    badgeSolid: `${c.bg100} ${c.text700}`,
    badgeBorder: `${c.bg100} ${c.text700} border ${c.border200}`,

    // Section header: soft bg + border
    sectionHeader: `${c.bg50} border-b ${c.border100}`,
    sectionTitle: `font-semibold ${c.text900}`,

    // Progress bar
    progressBar: `${c.bg500} rounded-full`,

    // Stat card
    statBg: `${c.bg50} border ${c.border100}`,
    statValue: `font-bold ${c.text700}`,
    statLabel: c.text600,
  };
}

