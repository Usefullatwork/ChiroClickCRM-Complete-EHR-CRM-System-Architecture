/**
 * Get macro content in specified language
 * @param {string} category - Category (adjustments, therapies, etc.)
 * @param {string} key - Macro key
 * @param {string} lang - Language code ('en' or 'no')
 * @returns {string} Macro text
 */
export function getMacroContent(category, key, lang = 'en') {
  const categoryData = MACRO_CONTENT[category];
  if (!categoryData) return '';

  const macroData = categoryData[key];
  if (!macroData) return '';

  return macroData[lang] || macroData['en'] || '';
}

/**
 * Get all macros for a category in specified language
 * @param {string} category - Category name
 * @param {string} lang - Language code
 * @returns {Object} All macros in category
 */
export function getCategoryMacros(category, lang = 'en') {
  const categoryData = MACRO_CONTENT[category];
  if (!categoryData) return {};

  const result = {};
  Object.entries(categoryData).forEach(([key, value]) => {
    result[key] = value[lang] || value['en'] || '';
  });

  return result;
}

/**
 * Build macro matrix data with translations
 * @param {string} lang - Language code
 * @returns {Object} Full macro matrix
 */
export function buildMacroMatrix(lang = 'en') {
  const matrix = {};

  Object.entries(MACRO_CONTENT).forEach(([category, macros]) => {
    matrix[category] = {};
    Object.entries(macros).forEach(([key, translations]) => {
      matrix[category][key] = translations[lang] || translations['en'] || '';
    });
  });

  return matrix;
}

